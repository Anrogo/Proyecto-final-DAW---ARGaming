            <h2 class="mt-4 text-primary">Un analista predice que PS5 venderá casi el doble que Xbox Series X para 2024</h2>

            <!-- Descripción -->
            <h2 class="small text-justify">La firma Ampere estima que PlayStation 5 venderá 66 millones de unidades para ese año, mientras que la consola de Microsoft colocará 37 millones; ambas venderían menos que sus predecesoras.</h2>

