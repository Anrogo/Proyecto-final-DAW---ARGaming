            <!-- Contenido del post -->

            <p class="text-justify">
                Sony venderá 66 millones de PlayStation 5 hacia finales de 2024 y Microsoft colocará 37 millones de Xbox Series X en las casas de los consumidores. La estimación viene de Piers Harding-Rolls, que ha realizado un informe para la firma Ampere del que se ha hecho eco el medio especializado GamesIndustry. El analista predice que ambas consolas saldrán al mismo precio y que venderán menos que sus predecesoras.
            </p>
            <p class="text-justify">
                Harding-Rolls espera que ambas consolas debuten estas Navidades al mismo precio, entre los 450 y los 500 dólares. Apunta, además, que en el estreno no habrá tanta diferencia en las ventas: PS5 venderá 4,6 millones de consolas para finales de 2020, mientras que Xbox Series X vendería 3,3 millones de unidades.
            </p>
            <p class="text-justify">
                El analista argumenta que Microsoft está mejor posicionada ahora que en el estreno de Xbox One por varios motivos: vende el aparato como un sistema orientado a jugadores (la campaña promocional de la anterior consola se centró mucho en sus funcionalidades como centro multimedia), ha invertido mucho en comprar estudios para tener juegos exclusivos, y tendrá un gran título acompañando el lanzamiento de la consola: Halo Infinite.
            </p>
            <hr>

            <img class="img-fluid rounded" src="/images/post/grafico.jpg" alt="foto-post">

            <p class="text-justify">
                Sin embargo, añade que el dominio actual del mercado de Sony sobre Microsoft, la lista de exclusivos presentados para PS5 (como Spider-Man: Miles Morales, Horizon 2: Forbidden West y Demon's Souls) y la lealtad de algunos jugadores a la marca PlayStation "son especialmente duros de desmantelar".
            </p>
            <p class="text-center text-white bg-dark font-italic"> El mercado de las consolas continuará decreciendo</p>
            <p class="text-justify">
                Desde Ampere consideran que las consolas seguirán siendo la forma principal para jugar a videojuegos durante la próxima generación gracias a las innovaciones en el hardware, el software y los modelos de negocio. Sin embargo, apuntan que el mercado de las consolas continuará disminuyendo.
            </p>
            <p class="text-justify">
                Harding-Rolls apunta (sin mencionar a las consolas de Nintendo) que las ventas totales combinadas de consolas de Sony y Microsoft alcanzaron su pico en la generación de los 128 bits, con Xbox y PlayStation 2, que vendieron 179 millones de unidades. El analista dice que PS3 y Xbox 360 alcanzaron los 171 millones; y finalmente que PS4 y Xbox One llegaron en marzo a los 157 millones. Conviene apuntar que Microsoft no da cifras oficiales de ventas de su última consola.
            </p>

            <img class="img-fluid rounded" src="/images/post/xbox-ps5-2.jpg" alt="foto-post">

            <p class="text-justify">
                "Aunque el mercado es aún sustancial y probablemente será consistentemente grande durante al menos el próximo ciclo de vida de las consolas", enuncia el informe de Ampere, "se ha probado durante la última década que incluso con recursos significativos hacia el crecimiento de la adopción global, incluyendo entradas más serias en territorios adicionales, el crecimiento sustancial en ventas de unidades de hardware para Sony y Microsoft combinadas no se ha materializado".
            </p>
            <p class="text-justify">
                PlayStation 5 y Xbox Series X llegarán a las tiendas estas Navidades.
            </p>

