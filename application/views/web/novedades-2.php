            <p class="text-justify">
                ¿Queréis Silent Hill? Poco a poco, Konami sigue mostrando colaboraciones de sus sagas más conocidas a los jugadores para llevarlas a un nuevo público, y en esta ocasión anuncia una nueva colaboración con Dead By Daylight, el juego online síncrono multijugador de terror en el que veremos algunas localizaciones y personajes de la conocida franquicia de Konami.
            </p>

            <hr>


            <iframe width="728" height="366" src="https://www.youtube.com/embed/N8VGnRN5-mc" frameborder="0" allow="accelerometer; autoplay; encrypted-media; gyroscope; picture-in-picture" allowfullscreen></iframe>

