            <p class="text-justify">
                Esta es la descripción oficial de esta colaboración, que llega durante junio al juego:
            </p>
            <p class="text-justify font-italic lead small">
                La emblemática franquicia de videojuegos se une al Reino de la Entidad y empuja aún más los límites del horror, presentando un nuevo mapa inspirado en Silent Hill , un monstruo aterrador y un misterioso sobreviviente.
            </p>

            <p class="text-justify font-italic lead small">
                La escuela primaria Midwich es el escenario de horrores indescriptibles. Cualquier sensación de inocencia que alguna vez marcó estos pasillos se ha ido. En su lugar: paredes manchadas, cadenas oxidadas, cadáveres colgantes y la sensación de que las cosas han salido terriblemente mal.
            </p>

            <p class="text-justify font-italic lead small">
                Pyramid Head, conocido como The Executioner, es un asesino sádico y despiadado obsesionado con imponer castigos a través del dolor. Nunca sin su enorme y gigantesca espada a cuestas, Pyramid Head se ha metido en la niebla para cumplir su nefasto deber.
            </p>

            <p class="text-justify font-italic lead small">
                Cheryl Mason, anteriormente conocida como Heather, es la nueva sobreviviente del Capítulo. Habiéndose liberado del culto religioso que la había perseguido desde su nacimiento, ahora debe librar una batalla eterna en el reino de la Entidad.
            </p>

