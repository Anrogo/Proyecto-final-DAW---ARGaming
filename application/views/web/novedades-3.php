            <hr>

            <p class="text-justify">
                Respecto a los elementos que compartirán Call of Duty: Modern Warfare y Warzone, encontramos al operador protagonista de la temporada 4, el Capitán Price. Más adelante se abrirán las puertas a Gaz y Rose. El arsenal se verá ampliado con la incorporación de las Fennec (SMG) y CR-56 AMAX (AR). Después podrás equiparte com las Rytec AMR (Sniper) y Kali Sticks.
            </p>
            <p class="text-justify">
                ¿Cuáles son las novedades de Warzone? El battle royale tendrá una variante: Juggernaut Royale. Consiste en un modo en el que tres paquetes dejarán el traje juggernaut en Verdansk. Los jugadores que logren obtenerlos tendrán salud más alta una mini pistola e incluso se activará una canción especial de fondo. Cabe señalar que, pese al peso del atuendo, los usuarios podrán subir a vehículos o escaleras. Asimismo, si caes desde una superficie alta no solo recibirás un daño mínimo, también perjudicarás a los contrincantes que estén cerca de la zona de impacto.
            </p>

            <img class="img-fluid rounded" src="/images/post/modern-warfare-2.webp" alt="foto-post">

            <p class="text-justify">
                Warzone también tendrá una modalidad realista en la que el HUD solo mostrará elementos esenciales. Además, se incrementa significativamente el daño a la cabeza. Recuerda que la cuarta temporada viene acompaña de un nuevo Season Pass, el cual te ofrecerá desbloquear todo tipo de objetos cosméticos como skins para armas y operadores, relojes, llaveros y puntos CP. En el siguiente tráiler puedes ver gran parte de los objetos disponibles en el pase de temporada de Call of Duty: Modern Warfare y Warzone:
