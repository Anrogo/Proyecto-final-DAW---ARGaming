            <p class="text-justify">
                Tras unos días de retraso debido a las protestas contra el racismo en Estados Unidos, Activision e Infinity Ward han lanzado la temporada 4 de Call of Duty: Modern Warfare y Call of Duty: Warzone. La puedes descargar desde ahora en PlayStation 4, Xbox One y PC. Desde luego, la cuarta etapa llega cargada de novedades tanto en el multijugador tradicional como en el battle royale. Las repasamos todas a continuación.
            </p>
            <p class="text-justify">
                En primer lugar hablaremos de Call of Duty: Modern Warfare. Su multiplayer da la bienvenida al mapa Scrapyard, uno de los más emblemáticos de Modern Warfare 2. No obstante, el estudio promete que en las próximas semanas agregará otros escenarios, entre ellos Cheshire Park. Por su parte, el modo Gunfight igualmente recibe un nuevo mapa: Trench. A lo largo de la temporada podrás disfrutar de nuevas modalidades como Team Defender, One in the Chamber y All or Nothing.
            </p>

            <hr>

